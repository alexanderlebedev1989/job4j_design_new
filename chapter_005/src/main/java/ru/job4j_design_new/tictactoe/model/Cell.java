public class Cell {

    private final char SIGN_X = 'x';
    private final char SIGN_O = 'o';
    private final char SIGN_EMPTY = '*';

    public char getSIGN_X() {
        return SIGN_X;
    }

    public char getSIGN_O() {
        return SIGN_O;
    }

    public char getSIGN_EMPTY() {
        return SIGN_EMPTY;
    }

}
